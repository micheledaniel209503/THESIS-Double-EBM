function alpha = alpha_sol(h, rc, params)
% soluzione constraint su alpha da Wolfram Mathematica
r1r = params.r1r;
r2r = params.r2r;
hr = params.hr;
ri = params.ri;
ro = params.ro;


alpha = (sqrt(hr.^2 + (r1r - r2r).^2).*(15.^(2./3).*r1r.^2 + 2.*15.^(2./3).*r1r.*r2r + 15.^(2./3).*r2r.^2 - ...
    15.^(1./3).*(-((9.*h.*(r1r + r2r).^2.*(rc.^2 + rc.*ri + ri.^2))./(hr.^2 + (r1r - r2r).^2)) + ...
       sqrt(3).*sqrt((1./(hr.^2 + (r1r - r2r).^2).^2).*((r1r + r2r).^4.*(5.*r1r.^6 - 10.*r1r.^5.*r2r - 5.*r1r.^4.*r2r.^2 + 20.*r1r.^3.*r2r.^3 - 5.*r1r.^2.*r2r.^4 - ...
            10.*r1r.*r2r.^5 + 5.*r2r.^6 + 5.*hr.^4.*(r1r + r2r).^2 + 10.*hr.^2.*(r1r.^2 - r2r.^2).^2 + 27.*h.^2.*rc.^4 + 54.*h.^2.*rc.^3.*ri + 81.*h.^2.*rc.^2.*ri.^2 + ...
            54.*h.^2.*rc.*ri.^3 + 27.*h.^2.*ri.^4)))).^(2./3)))./ ...
  (6.*(r1r + r2r).*(-((9.*h.*(r1r + r2r).^2.*(rc.^2 + rc.*ri + ri.^2))./(hr.^2 + (r1r - r2r).^2)) + ...
     sqrt(3).*sqrt((1./(hr.^2 + (r1r - r2r).^2).^2).*((r1r + r2r).^4.*(5.*r1r.^6 - 10.*r1r.^5.*r2r - 5.*r1r.^4.*r2r.^2 + 20.*r1r.^3.*r2r.^3 - 5.*r1r.^2.*r2r.^4 - ...
          10.*r1r.*r2r.^5 + 5.*r2r.^6 + 5.*hr.^4.*(r1r + r2r).^2 + 10.*hr.^2.*(r1r.^2 - r2r.^2).^2 + 27.*h.^2.*rc.^4 + 54.*h.^2.*rc.^3.*ri + 81.*h.^2.*rc.^2.*ri.^2 + ...
          54.*h.^2.*rc.*ri.^3 + 27.*h.^2.*ri.^4)))).^(1./3));


end